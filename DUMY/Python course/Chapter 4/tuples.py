# Tuples
dimensions = (200, 50)
print(dimensions[0])
print(dimensions[1])

# Looping Through All Values in a Tuple

dimensions = (200, 50)
for dimension in dimensions:
    print(dimension)

# Writing over a tuple

dimensions = (200, 50)
print("Original dimensions:")
for dimension in dimensions:
    print(dimension)
    
# line len
dimensions = (200, 50)
dimensions = (400, 100)
print("Modified dimensions:")
for dimension in dimensions:
    print(dimension)
    
